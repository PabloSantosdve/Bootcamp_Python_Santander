# Operadores de Atribuição
"""Operadores de atribuição são usados para atribuir valores a variáveis.
Eles podem ser usados para atribuir valores simples ou realizar operações ao mesmo tempo.
Operadores de atribuição são usados para atribuir valores a variáveis.
"""

# Atribuição simples
saldo = 500
print(f"Saldo final: {saldo}")

# Atribuição com adição
saldo += 100  # Equivalente a saldo = saldo + 100
print(f"Saldo final: {saldo}")

# Atribuição com subtração
saldo -= 50   # Equivalente a saldo = saldo - 50
print(f"Saldo final: {saldo}")

# Atribuição com multiplicação
saldo *= 2    # Equivalente a saldo = saldo * 2
print(f"Saldo final: {saldo}")

# Atribuição com divisão
saldo /= 4    # Equivalente a saldo = saldo / 4
print(f"Saldo final: {saldo}")

# Atribuição com divisão inteira
saldo //= 2   # Equivalente a saldo = saldo // 2
print(f"Saldo final: {saldo}")

# Atribuição com módulo
saldo %= 3    # Equivalente a saldo = saldo % 3
print(f"Saldo final: {saldo}")

# Atribuição com exponenciação
saldo **= 2   # Equivalente a saldo = saldo ** 2
print(f"Saldo final: {saldo}")

