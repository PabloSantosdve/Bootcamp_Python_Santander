produto_1 = 10
produto_2 = 20

print(produto_1 + produto_2)  # Soma
print(produto_1 - produto_2)  # Subtração   
print(produto_1 * produto_2)  # Multiplicação
print(produto_1 / produto_2)  # Divisão
print(produto_1 // produto_2)  # Divisão inteira
print(produto_1 % produto_2)  # Módulo


#Modulo é o resto da divisão, 
# # Exemplo: 10 % 3 = 1, pois 10 dividido por 3 é 3 com resto 1
print(produto_1 ** produto_2)  # Exponenciação
# Exemplo: 10 ** 2 = 100, pois 10 elevado a 2 é 100
print(produto_1 + produto_2 * 2)  # Operadores de precedência
# Exemplo: 10 + 20 * 2 = 50, pois a multiplicação é feita antes da adição
print(produto_1 + (produto_2 * 2))  # Uso de parênteses
# Exemplo: 10 + (20 * 2) = 50, pois os parênteses alteram a precedência
# Exemplo de uso de operadores aritméticos com variáveis
preco_produto_1 = 15.99
preco_produto_2 = 25.49
total = preco_produto_1 + preco_produto_2
print(f"Total dos produtos: {total:.2f}")  # Formatação de saída com duas casas decimais

