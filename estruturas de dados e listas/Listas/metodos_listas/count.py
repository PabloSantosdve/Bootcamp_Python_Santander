# O método .count() é usado para contar quantas vezes um determinado valor
# aparece dentro de uma lista. Ele retorna um número inteiro como resultado.


# Criamos uma lista com nomes de cores.
# "azul" aparece duas vezes nessa lista.
cores = ["vermelho", "azul", "verde", "azul"]

# O método .count("valor") conta quantas vezes o valor aparece na lista.

print(cores.count("vermelho"))  # Saída: 1
# "vermelho" aparece uma vez na lista.

print(cores.count("azul"))  # Saída: 2
# "azul" aparece duas vezes na lista.

print(cores.count("verde"))  # Saída: 1
# "verde" aparece uma vez na lista.

