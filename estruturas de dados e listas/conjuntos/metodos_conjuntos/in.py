# Criando um conjunto com números, eliminando duplicatas automaticamente
numeros = {1, 2, 3, 1, 2, 4, 5, 5, 6, 7, 8, 9, 0}

# Verificando se o número 1 está no conjunto
# O operador 'in' retorna True se o elemento estiver presente no conjunto
print(1 in numeros)  # Saída: True

# Verificando se o número 10 está no conjunto
# Como 10 não está presente, o resultado será False
print(10 in numeros)  # Saída: False
