# O método .reverse() inverte a ordem dos elementos da lista original.
# Ele modifica a lista diretamente (in-place), sem criar uma nova lista.

linguagens = ["python", "js", "c", "java", "csharp"]

# Inverte a ordem dos elementos na lista.
linguagens.reverse()

# Exibe a lista invertida.
print(linguagens)  # Saída: ["csharp", "java", "c", "js", "python"]
