# Criando um conjunto com números repetidos
# O conjunto elimina automaticamente as duplicatas
numeros = {1, 2, 3, 1, 2, 4, 5, 5, 6, 7, 8, 9, 0}

# A função len() retorna a quantidade de elementos únicos no conjunto.
# Mesmo que valores repetidos tenham sido inseridos, o conjunto armazena apenas uma cópia de cada.
print(len(numeros))  # Saída: 10, porque existem 10 elementos diferentes
