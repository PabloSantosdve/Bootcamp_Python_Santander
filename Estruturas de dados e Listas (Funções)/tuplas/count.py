# O método .count() conta quantas vezes um valor aparece na tupla.
# É útil para saber a frequência de um elemento específico.

cores = (
    "vermelho",
    "azul",
    "verde",
    "azul",
)

# Conta quantas vezes "vermelho" aparece na tupla (1 vez)
print(cores.count("vermelho"))  # Saída: 1

# Conta quantas vezes "azul" aparece (2 vezes)
print(cores.count("azul"))  # Saída: 2

# Conta quantas vezes "verde" aparece (1 vez)
print(cores.count("verde"))  # Saída: 1
